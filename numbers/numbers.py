# Numbers exercises

a, b = 19, 13.3

print(a + b, a - b, a * b, a / b)  # 32.3 5.699999999999999 252.70000000000002 1.4285714285714286
print(b // 2)  # 6.0 (Floor division)

print(7 % 2)  # 1 (Modulus)
print(a ** 2)  # 361 (Exponentiation, Power of)

print(abs(-5), round(b), pow(a, 2))  # 5 13 361 (Absolute, Round to the nearest integer, Power of)

print(float(a), int(b))  # 19.0 13 (Type conversion)
